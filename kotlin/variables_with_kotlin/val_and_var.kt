fun main() {
	// mutated variable in kotlin :
	// this are the variable's value can be change just like a variable in c or c++
	// we use ' var ' keyword for a mutable variable 
	//example : 
	var a =3
    a = 6
    println(a)
    
    //immutable variable in kotlin:
    //this are the variable's value can not be changed just like a constant in c or c++
    //we use ' val ' keyword for a immutable variable
    //example:
    val name = "Toushik"
    // name = "Banik" - you cant do things like this with val
    println(name)
}
